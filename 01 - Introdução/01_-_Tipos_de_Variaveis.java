public class Variaveis {
	
	// Cada public static como se fosse um Projeto (Fins Didaticos)
	public static void main(String[] args) {
		byte varTipoByte = 9;
		short varTipoShort = 1000;
		int varTipoInt = 100000;
		
		long varTipoLong = 1000000;
		long numGrande = 2000000000L; // L => denotação para Long
		
		int valor; // inicia com valor padrao de 0
	}

	public static void main(String[] args) {
		int a = 20;
		int b = 10;
		int soma = a + b;
		int subtracao = a - b;
		int divisao = a / b;
		int multiplicacao = a * b;
		int resto = a % b;

		System.out.println(soma); // 30
		System.out.println(subtracao); // 10
		System.out.println(divisao); // 2
		System.out.println(multiplicacao); // 200
		System.out.println(resto); // 0
	}
}
